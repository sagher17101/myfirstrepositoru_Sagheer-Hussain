
# Upper case 
x = "my name is Sagheer and my father name is Ameer"
print(x.upper())

#Lowercase
x = "MY NAME IS Sagheer AND MY FATHER NAME IS Ameer"
print(x.lower())

# Removing Whitespace

x = "   Sagheer, Hussain "
print(x.strip())  

# Replace string 

x = "This is data  science tutorials"
print(x.replace("This" , "Bahi"))

# Split string 

x = "This, is, data , science, tutorials"
print(x.split(","))


